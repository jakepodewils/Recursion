func factorial(fNumber: Int)-> Int {
    if n <= 1 {
        print("Calculating factorial(\(n)).")
        print("Terminal case, returning 1.")
        print("The final result is 1.")
        return 1
    } else {
        print("Calculating factorial(\(n)).")
        let calc = n * n-1
        factorial(fNumber: n-1)
        print("Terminal case, returning 1.")
        print("Non-Terminal case, returning \(calc)")
    }
}
