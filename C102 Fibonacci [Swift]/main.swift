func fib(int: Int) -> Int {
    if(int <= 2) {
        print("Calculating fibonacci(\(int))...")
        print("Terminal case, returning 1.")
        return 1
    } else {
        print("Calculating fibonacci(\(int))...")
        let ans = fib(int: int-1) + fib(int: int-2)
        print("Non-terminal case, returning \(ans).")
        return ans
    }
}
